#include <mymath.h>

int addTwoNums(int firstNumber, int secondNumber){
    int sum = firstNumber + secondNumber;
    return sum;
}

int subTwoNums(int firstNumber, int secondNumber){
    int sub = firstNumber - secondNumber;
    return sub;
}

int multiNums(int firstNumber, int secondNumber){
    int multi = firstNumber * secondNumber;
    return multi;
}